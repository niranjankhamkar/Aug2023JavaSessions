package org.basicProgram;

public class SwitchCase {

	public static void main(String[] args) {

		int monthNum = 5;
		switch (monthNum) {

		case (1): {
			System.out.println("Jan");
			break;
			}
		case (2): {
			System.out.println("feb");
			break;
			}
		case (3): {
			System.out.println("Mar");
			break;
			}
		case (4): {
			System.out.println("Apr");
			break;
			}
		case (5): {
			System.out.println("May");
			break;
			}
		}
	}
}
